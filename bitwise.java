class bitwise
{
	public static void main(String[] args)
	{
		int a=10,b=20,c;
		System.out.println("a&b="+(a&b));
		System.out.println("a|b="+(a|b));
		System.out.println("a^b="+(a^b));
		System.out.println("~a="+(~a));
		c=a<<2;
		System.out.println("a<<2  is"+c);
		c=a>>2;
		System.out.println("2>>a is"+c);		
		
	}
}